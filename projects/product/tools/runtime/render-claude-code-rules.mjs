#!/usr/bin/env node

import fs from 'node:fs';
import path from 'node:path';
import process from 'node:process';
import { assertRuntimeTargetPath } from './adapter-contract.mjs';

const MANAGED_PREFIX = '<!-- Generated by Buildr.';
const MANAGED_BLOCK_START_PREFIX = '<!-- BEGIN Buildr managed Claude Code rules bridge';
const MANAGED_BLOCK_END = '<!-- END Buildr managed Claude Code rules bridge -->';
const RULE_DISCOVERY_EXCLUDED_DIRECTORIES = new Set([
  '.git',
  '.hg',
  '.svn',
  '.agents',
  '.claude',
  '.codex',
  '.cursor',
  '.trae',
  '.worktrees',
  'node_modules',
  'vendor',
  'dist',
  'build',
  'coverage',
]);

function usage(command = 'buildr rules render claude-code') {
  console.error(`Usage: ${command} --scope <.|projects/project[/services/service[/path...]]> --target <dir>`);
}

export function parseRenderClaudeCodeRulesArgs(argv, command) {
  const args = {};
  for (let i = 0; i < argv.length; i += 1) {
    const arg = argv[i];
    if (arg === '--scope' || arg === '--target') {
      const value = argv[i + 1];
      if (!value || value.startsWith('--')) {
        throw new Error(`Missing value for ${arg}`);
      }
      args[arg.slice(2)] = value;
      i += 1;
    } else {
      throw new Error(`Unknown argument: ${arg}`);
    }
  }
  if (!args.scope || !args.target) {
    usage(command);
    throw new Error('Missing required arguments');
  }
  return args;
}

function normalizeRelativePath(input) {
  if (typeof input !== 'string' || !input.trim()) throw new Error('Scope must be a workspace-relative path.');
  const normalized = path.normalize(input.trim()).replace(/^\.\//, '') || '.';
  if (path.isAbsolute(normalized) || normalized === '..' || normalized.startsWith(`..${path.sep}`)) {
    throw new Error(`Scope must stay inside the workspace: ${input}`);
  }
  return normalized;
}

function ensureDirectory(dir, message) {
  if (!fs.existsSync(dir) || !fs.statSync(dir).isDirectory()) {
    throw new Error(message);
  }
}

function ensureScopeStaysInsideWorkspace(repoRoot, parts, scopeInput) {
  let current = repoRoot;
  for (const part of parts) {
    if (part === '.') continue;
    current = path.join(current, part);
    if (fs.existsSync(current) && fs.lstatSync(current).isSymbolicLink()) {
      throw new Error(`Scope must not traverse a symbolic link: ${scopeInput}`);
    }
  }
  const workspaceRealPath = fs.realpathSync(repoRoot);
  const scopeRealPath = fs.realpathSync(current);
  const relative = path.relative(workspaceRealPath, scopeRealPath);
  if (relative === '..' || relative.startsWith(`..${path.sep}`) || path.isAbsolute(relative)) {
    throw new Error(`Scope must stay inside the workspace: ${scopeInput}`);
  }
}

export function resolveRuleScope(repoRoot, scopeInput) {
  const requestedScope = normalizeRelativePath(scopeInput);
  let scope = requestedScope;
  let parts = scope === '.' ? ['.'] : scope.split(path.sep);
  const warnings = [];
  const organizationRoot = repoRoot;
  let projectRoot = null;
  let serviceRoot = null;
  let scopeRoot;

  if (parts[0] === '.') {
    scopeRoot = repoRoot;
  } else if (parts[0] === 'projects') {
    if (parts.length < 2 || !parts[1]) throw new Error(`Project scope must start with projects/<project>: ${scopeInput}`);
    projectRoot = path.join(repoRoot, 'projects', parts[1]);
    ensureDirectory(projectRoot, `Project scope does not exist: ${toPosixRelative(repoRoot, projectRoot)}`);

    if (parts.length > 2 && parts[2] !== 'services') {
      const registeredServices = readRegistryIds(path.join(projectRoot, 'services', 'manifest.yml'), 'services');
      const legacyService = parts[2];
      if (registeredServices.has(legacyService)) {
        const literalRoot = path.join(repoRoot, ...parts);
        const canonicalParts = ['projects', parts[1], 'services', ...parts.slice(2)];
        const canonicalRoot = path.join(repoRoot, ...canonicalParts);
        const literalExists = isDirectory(literalRoot);
        const canonicalExists = isDirectory(canonicalRoot);
        if (literalExists && canonicalExists) {
          throw new Error(`Ambiguous legacy Service scope: ${scopeInput}. Use canonical workspace path ${canonicalParts.join('/')}.`);
        }
        if (!literalExists) {
          scope = canonicalParts.join(path.sep);
          parts = canonicalParts;
          warnings.push(`旧 Service scope ${requestedScope} 已兼容解析为 ${toPosixPath(scope)}；请改用 canonical workspace 相对路径。`);
        }
      }
    }

    scopeRoot = path.join(repoRoot, ...parts);
    if (parts[2] === 'services' && parts[3]) {
      serviceRoot = path.join(projectRoot, 'services', parts[3]);
    }
  } else {
    throw new Error(`Unsupported scope. Use . or a real workspace path under projects/<project>: ${scopeInput}`);
  }

  for (const part of parts) {
    if (RULE_DISCOVERY_EXCLUDED_DIRECTORIES.has(part)) {
      throw new Error(`Scope enters an excluded directory: ${scopeInput}`);
    }
  }
  ensureDirectory(scopeRoot, `Scope directory does not exist: ${toPosixRelative(repoRoot, scopeRoot)}`);
  ensureScopeStaysInsideWorkspace(repoRoot, parts, scopeInput);

  return {
    scope: toPosixPath(scope),
    requestedScope: toPosixPath(requestedScope),
    organizationRoot,
    projectRoot,
    serviceRoot,
    scopeRoot,
    warnings,
  };
}

function toPosixPath(value) {
  return value.split(path.sep).join('/');
}

function isDirectory(directory) {
  return fs.existsSync(directory) && fs.statSync(directory).isDirectory();
}

function isFile(file) {
  return fs.existsSync(file) && fs.statSync(file).isFile();
}

function readRegistryIds(file, collectionKey) {
  const ids = new Set();
  if (!isFile(file)) return ids;
  let inCollection = false;
  for (const rawLine of fs.readFileSync(file, 'utf8').split(/\r?\n/)) {
    if (!inCollection) {
      if (new RegExp(`^${collectionKey}:\\s*$`).test(rawLine)) inCollection = true;
      continue;
    }
    if (/^[^\s]/.test(rawLine)) break;
    const match = rawLine.match(/^  ([A-Za-z0-9._-]+):(?:\s.*)?$/);
    if (match) ids.add(match[1]);
  }
  return ids;
}

function managedNestedGitRoots(repoRoot) {
  const roots = new Set();
  const projectIds = readRegistryIds(path.join(repoRoot, 'projects', 'manifest.yml'), 'projects');
  for (const projectId of projectIds) {
    const projectRoot = path.join(repoRoot, 'projects', projectId);
    roots.add(path.resolve(projectRoot));
    const serviceIds = readRegistryIds(path.join(projectRoot, 'services', 'manifest.yml'), 'services');
    for (const serviceId of serviceIds) {
      roots.add(path.resolve(projectRoot, 'services', serviceId));
    }
  }
  return roots;
}

function discoveryBoundary(repoRoot, directory, reason) {
  return { path: toPosixRelative(repoRoot, directory), reason };
}

function discoverDescendantFiles(repoRoot, scopeInfo, basename) {
  const files = [];
  const boundaries = [];
  const managedGitRoots = managedNestedGitRoots(repoRoot);

  function walk(directory) {
    if (isFile(path.join(directory, basename))) files.push(path.join(directory, basename));
    const entries = fs.readdirSync(directory, { withFileTypes: true })
      .sort((left, right) => left.name.localeCompare(right.name));
    for (const entry of entries) {
      if (!entry.isDirectory() && !entry.isSymbolicLink()) continue;
      const child = path.join(directory, entry.name);
      if (entry.isSymbolicLink()) {
        boundaries.push(discoveryBoundary(repoRoot, child, 'symbolic-link'));
        continue;
      }
      if (RULE_DISCOVERY_EXCLUDED_DIRECTORIES.has(entry.name)) {
        boundaries.push(discoveryBoundary(repoRoot, child, 'excluded-directory'));
        continue;
      }
      if (fs.existsSync(path.join(child, '.git')) && !managedGitRoots.has(path.resolve(child))) {
        boundaries.push(discoveryBoundary(repoRoot, child, 'unregistered-nested-git-repository'));
        continue;
      }
      walk(child);
    }
  }

  walk(scopeInfo.scopeRoot);
  return { files, boundaries };
}

function ancestorDirectories(repoRoot, scopeRoot) {
  const relative = path.relative(repoRoot, scopeRoot);
  const directories = [repoRoot];
  if (!relative) return directories;
  let current = repoRoot;
  for (const segment of relative.split(path.sep)) {
    current = path.join(current, segment);
    directories.push(current);
  }
  return directories;
}

export function buildRuleDiscoveryPlan(repoRoot, scopeInfo) {
  const sources = new Set();
  for (const directory of ancestorDirectories(repoRoot, scopeInfo.scopeRoot)) {
    const source = path.join(directory, 'AGENTS.md');
    if (isFile(source)) sources.add(source);
  }
  const descendants = discoverDescendantFiles(repoRoot, scopeInfo, 'AGENTS.md');
  for (const source of descendants.files) sources.add(source);
  const files = [...sources].sort((left, right) => {
    const leftRelative = path.relative(repoRoot, left);
    const rightRelative = path.relative(repoRoot, right);
    const depthDifference = leftRelative.split(path.sep).length - rightRelative.split(path.sep).length;
    return depthDifference || toPosixPath(leftRelative).localeCompare(toPosixPath(rightRelative));
  });
  return {
    scope: scopeInfo.scope,
    requestedScope: scopeInfo.requestedScope,
    files,
    boundaries: descendants.boundaries,
    warnings: [...scopeInfo.warnings],
  };
}

export function resolveAgentFiles(repoRoot, scopeInfo) {
  return buildRuleDiscoveryPlan(repoRoot, scopeInfo).files;
}

export function discoverManagedOrphanRuleBridges(repoRoot, scopeInfo) {
  const descendants = discoverDescendantFiles(repoRoot, scopeInfo, 'CLAUDE.md');
  const files = [];
  for (const targetFile of descendants.files) {
    if (isFile(path.join(path.dirname(targetFile), 'AGENTS.md'))) continue;
    const content = fs.readFileSync(targetFile, 'utf8');
    if (hasManagedRulesMarker(content)) files.push(targetFile);
  }
  return { files, boundaries: descendants.boundaries };
}

export function hasManagedRulesMarker(content) {
  const firstLine = content.split(/\r?\n/)[0] ?? '';
  return content.includes(MANAGED_BLOCK_START_PREFIX) || firstLine.startsWith(MANAGED_PREFIX) || content.includes('此文件为 Claude Code 桥接文件。');
}

export function buildConflictMessage(targetFile) {
  return [
    `CLAUDE.md already exists and is not Buildr-managed: ${targetFile}`,
    'Choose a merge plan:',
    '1. Add a Buildr managed block to this file, then rerun this command.',
    '2. Move long-term rules into the relevant Buildr AGENTS.md, then rerun this command.',
    '3. Keep Claude Code-only custom content outside the Buildr managed block.',
  ].join('\n');
}

function toPosixRelative(from, to) {
  const relative = path.relative(from, to) || path.basename(to);
  return relative.split(path.sep).join('/');
}

export function buildRulesTarget(repoRoot, targetRoot, agentFile) {
  return path.join(path.dirname(agentFile), 'CLAUDE.md');
}

export function buildClaudeManagedBlock(repoRoot, targetFile, agentFile) {
  const lines = [
    '<!-- BEGIN Buildr managed Claude Code rules bridge; type: reference; source: AGENTS.md -->',
    '@AGENTS.md',
    MANAGED_BLOCK_END,
    '',
  ];
  return lines.join('\n');
}

export function buildClaudeBridge(repoRoot, targetFile, agentFile) {
  return `# CLAUDE.md\n\n${buildClaudeManagedBlock(repoRoot, targetFile, agentFile)}`;
}

function replaceManagedBlock(existingContent, managedBlock) {
  const start = existingContent.indexOf(MANAGED_BLOCK_START_PREFIX);
  const end = existingContent.indexOf(MANAGED_BLOCK_END);
  if (start !== -1 && end !== -1 && end > start) {
    const afterEnd = end + MANAGED_BLOCK_END.length;
    return `${existingContent.slice(0, start)}${managedBlock.trimEnd()}${existingContent.slice(afterEnd)}`;
  }

  const lines = existingContent.split(/\r?\n/);
  const firstLine = lines[0] ?? '';
  if (firstLine.startsWith(MANAGED_PREFIX) || existingContent.includes('此文件为 Claude Code 桥接文件。')) {
    const hashLineIndex = lines.findIndex((line) => line.startsWith('<!-- Buildr source hash:'));
    const customSuffix = hashLineIndex === -1 ? '' : lines.slice(hashLineIndex + 1).join('\n').trim();
    return customSuffix ? `${managedBlock}\n${customSuffix}\n` : managedBlock;
  }

  return `${managedBlock}\n${existingContent}`;
}

function removeManagedBlock(existingContent) {
  const start = existingContent.indexOf(MANAGED_BLOCK_START_PREFIX);
  const end = existingContent.indexOf(MANAGED_BLOCK_END);
  if (start === -1 || end === -1 || end <= start) return null;
  const afterEnd = end + MANAGED_BLOCK_END.length;
  const content = `${existingContent.slice(0, start)}${existingContent.slice(afterEnd)}`
    .replace(/^# CLAUDE\.md\s*$/m, '')
    .trim();
  return content ? `${content}\n` : '';
}

export function planRulesRender(repoRoot, targetRoot, scopeInfo) {
  const discovery = buildRuleDiscoveryPlan(repoRoot, scopeInfo);
  const agentFiles = discovery.files;
  if (agentFiles.length === 0) {
    throw new Error(`No AGENTS.md files found for scope: ${scopeInfo.scope}`);
  }

  const conflicts = [];
  const actions = [];
  for (const agentFile of agentFiles) {
    const targetFile = buildRulesTarget(repoRoot, targetRoot, agentFile);
    let existingContent = null;
    if (fs.existsSync(targetFile)) {
      existingContent = fs.readFileSync(targetFile, 'utf8');
      if (!hasManagedRulesMarker(existingContent)) {
        conflicts.push(buildConflictMessage(targetFile));
        continue;
      }
    }

    const managedBlock = buildClaudeManagedBlock(repoRoot, targetFile, agentFile);
    const content = existingContent !== null
      ? replaceManagedBlock(existingContent, managedBlock)
      : buildClaudeBridge(repoRoot, targetFile, agentFile);
    actions.push({
      action: existingContent === null ? 'created' : existingContent === content ? 'unchanged' : 'updated',
      sourceFile: agentFile,
      targetFile,
      content,
    });
  }

  const orphanDiscovery = discoverManagedOrphanRuleBridges(repoRoot, scopeInfo);
  for (const targetFile of orphanDiscovery.files) {
    const existingContent = fs.readFileSync(targetFile, 'utf8');
    const preservedContent = removeManagedBlock(existingContent);
    actions.push({
      action: preservedContent ? 'updated' : 'removed',
      sourceFile: null,
      targetFile,
      content: preservedContent || null,
    });
  }
  return {
    targetRoot,
    actions,
    conflicts,
    discovery: {
      ...discovery,
      boundaries: [...discovery.boundaries, ...orphanDiscovery.boundaries]
        .filter((boundary, index, all) => all.findIndex((item) => item.path === boundary.path && item.reason === boundary.reason) === index),
    },
  };
}

export function applyRulesRenderPlan(plan) {
  if (plan.conflicts.length > 0) {
    throw new Error(`Rules render found ${plan.conflicts.length} conflict(s); no files were changed.\n\n${plan.conflicts.join('\n\n')}`);
  }
  for (const item of plan.actions) assertRuntimeTargetPath(plan.targetRoot, item.targetFile, 'Runtime Rules target');
  for (const item of plan.actions) {
    if (item.action === 'unchanged') continue;
    if (item.action === 'removed') {
      fs.rmSync(item.targetFile);
      continue;
    }
    fs.mkdirSync(path.dirname(item.targetFile), { recursive: true });
    fs.writeFileSync(item.targetFile, item.content, 'utf8');
  }
  return plan;
}

function renderRules(repoRoot, targetRoot, scopeInfo) {
  return applyRulesRenderPlan(planRulesRender(repoRoot, targetRoot, scopeInfo));
}

export function renderClaudeCodeRules(argv, options = {}) {
  const repoRoot = options.repoRoot ?? process.cwd();
  const command = options.command ?? 'buildr rules render claude-code';
  const args = parseRenderClaudeCodeRulesArgs(argv, command);
  const scopeInfo = resolveRuleScope(repoRoot, args.scope);
  const targetRoot = path.resolve(repoRoot, args.target);
  ensureDirectory(targetRoot, `Target directory does not exist: ${targetRoot}`);

  const rendered = renderRules(repoRoot, targetRoot, scopeInfo);
  return {
    targetRoot,
    scope: scopeInfo.scope,
    scopeInfo,
    files: rendered.actions.map((item) => item.targetFile),
    actions: rendered.actions,
    discovery: rendered.discovery,
    warnings: rendered.discovery.warnings,
  };
}

function main() {
  const { targetRoot, actions, warnings } = renderClaudeCodeRules(process.argv.slice(2), {
    command: 'node tools/runtime/render-claude-code-rules.mjs',
  });
  for (const warning of warnings) console.error(`Warning: ${warning}`);
  for (const item of actions) {
    console.log(`${item.action} ${toPosixRelative(targetRoot, item.targetFile)}`);
  }
}

if (import.meta.url === `file://${process.argv[1]}`) {
  try {
    main();
  } catch (error) {
    console.error(error.message);
    process.exit(1);
  }
}
